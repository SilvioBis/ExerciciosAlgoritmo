/*1) Elabore um algoritmo que leia 3 números e apresente o resultado da soma do primeiro pelo segundo multiplicado pelo terceiro.*/
var num1 = parseInt(prompt("Digite o primeiro numero"));
var num2 = parseInt(prompt("Digite o seguindo numero"));
var num3 = parseInt(prompt("Digite o terceiro numero"));

var resultado = (num1 + num2) * num3 ;

alert("Resultado é: " + resultado); 
